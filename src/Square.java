/**
 * Essentially a container for a row/column ordered pair
 */
public class Square {
    int row;
    int col;

    /**
     * Constructs a Square with a row and a column
     * @param row
     * @param col
     */
    public Square(int row, int col) {
        this.row = row;
        this.col = col;
    }
}
